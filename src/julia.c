#include "fractol.h"

int	julia(t_pt2 j, t_fractal *f)
{
	t_complex z;
	int i;

	z.r = 1.5 * (j.x - f->e.w / 2) / (0.5 * 1 * f->e.w);
	z.i = (j.y - f->e.h / 2) / (0.5 * 1 * f->e.h);
	i = 0;
	while (i < f->i && c_modulus(z) < 2)
	{
		z = c_add(c_product(z, z), f->c);
		i++;
	}
	//return (i == f->i ? BLACK : WHITE);
	return (i * 1000);
}
